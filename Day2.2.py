'''Arithmetic Operator for performing calculations and Building a Static Calculator'''
''' +, -, *, /, //, %, ** are used for performing addition, subtraction, multiplication, division, round fig division,
modular division, power'''

a = 10
b = 3
print("Sum: ", a+b,"Difference: ", a-b,"Product: ", a*b,"Quotient: ", a/b,"Round Fig Difference: ", a//b, sep = "\n")
print("Remainder: ", a%b,"Power Result: ", a**b, sep = "\n")